function contagemCesta(cesta) {
  let cestaObj = {};
  for (let i = 0; i < cesta.length; i += 1) {
    if (cestaObj[cesta[i]] === undefined) {
    cestaObj[cesta[i]] = 1;
  }
    else {
      cestaObj[cesta[i]] += 1;
    }
  }
  let fraseFinal = 'Sua cesta possui: '
  for (let key in cestaObj) {
    fraseFinal += ' ' + cestaObj[key] + ' ' + key + '(s),';
  }
  fraseFinal = fraseFinal.replace(/.$/, ".");
  return fraseFinal;
}

